set -e
mysql -h"${MYSQL_HOST:-localhost}" -uroot -p$MYSQL_ROOT_PASSWORD <<EOF
CREATE DATABASE IF NOT EXISTS $MYSQL_EALERT_DATABASE;
CREATE USER IF NOT EXISTS "$MYSQL_EALERT_USERNAME"@'%' IDENTIFIED BY "$MYSQL_EALERT_PASSWORD";
GRANT ALL PRIVILEGES ON $MYSQL_EALERT_DATABASE.* TO "$MYSQL_EALERT_USERNAME"@'%';

CREATE DATABASE IF NOT EXISTS $MYSQL_ETASK_DATABASE;
CREATE USER IF NOT EXISTS "$MYSQL_ETASK_USERNAME"@'%' IDENTIFIED BY "$MYSQL_ETASK_PASSWORD";
GRANT ALL PRIVILEGES ON $MYSQL_ETASK_DATABASE.* TO "$MYSQL_ETASK_USERNAME"@'%';
FLUSH PRIVILEGES;
EOF